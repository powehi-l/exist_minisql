dberr_t ExecuteEngine::ExecuteUseDatabase(pSyntaxNode ast, ExecuteContext *context) {
#ifdef ENABLE_EXECUTE_DEBUG
  LOG(INFO) << "ExecuteUseDatabase" << std::endl;
#endif
    if (!dbs_[ast->child_->val_]){
        cout << "Unknown database '" << ast->child_->val_ << "'";
        return DB_FAILED;
    }
    current_db_ = ast->child_->val_;
    current_db = dbs_[current_db_];
    return DB_SUCCESS;
//  return DB_FAILED;
}

dberr_t ExecuteEngine::ExecuteShowTables(pSyntaxNode ast, ExecuteContext *context) {
#ifdef ENABLE_EXECUTE_DEBUG
  LOG(INFO) << "ExecuteShowTables" << std::endl;
#endif
    if (!current_db) {
        cout << "No database selected";
        return DB_FAILED;
    }
    vector<TableInfo *> tables;
    current_db->catalog_mgr_->GetTables(tables);
    if (tables.empty()) {
        cout << "Empty set";
        return DB_FAILED;
    }
    cout << "+--------------------+" << endl;
    cout << " Tables_in_" << current_db_ << endl;
    cout << "+--------------------+" << endl;
    for(auto t = tables.begin(); t < tables.end(); t++)
        cout << " " << (*t)->GetTableName() << endl;
    cout << "+--------------------+" << endl;
    return DB_SUCCESS;
//  return DB_FAILED;
}

